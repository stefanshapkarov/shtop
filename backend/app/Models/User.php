<?php

namespace App\Models;

// use Illuminate\Contracts\Auth\MustVerifyEmail;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;
use Laravel\Fortify\TwoFactorAuthenticatable;
use Laravel\Sanctum\HasApiTokens;

class User extends Authenticatable
{
    use HasApiTokens, HasFactory, Notifiable;

    /**
     * The attributes that are mass assignable.
     *
     * @var array<int, string>
     */
    protected $fillable = [
        'name',
        'email',
        'password',
        'profile_picture',
        'phone_number',
        'birth_date',
        'bio',
        'location'
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
        'password' => 'hashed',
    ];

    public function drivenRidePosts()
    {
        return $this->hasMany(RidePost::class, 'driver_id');
    }

    public function passengerRidePosts()
    {
        return $this->belongsToMany(RidePost::class,
            'ride_post_passenger', 'passenger_id', 'ride_post_id');
    }

    public function reviewsGiven()
    {
        return $this->hasMany(Review::class, 'reviewer_id');
    }

    public function reviewsReceived()
    {
        return $this->hasMany(Review::class, 'reviewee_id');
    }

    public function getRatingAsDriver()
    {
        return $this->reviewsReceived()->whereHas('ride', function ($query) {
            $query->where('driver_id', $this->id);
        })
            ->avg('rating');
    }

    public function getRatingAsPassenger()
    {
        return $this->reviewsReceived()
            ->whereHas('ride', function ($query) {
                $query->whereHas('passengers', function ($query) {
                    $query->where('passenger_id', $this->id);
                });
            })
            ->avg('rating');
    }
}
